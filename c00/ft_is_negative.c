#include <unistd.h>

void	ft_is_negative(int n)
{
	char	ne;

	if (n >= 0)
	{
		ne = 'P';
		write(1, &ne, 1);
	}
	else
	{
		ne = 'N';
		write(1, &ne, 1);
	}
}
